#While loop
#increamenting values
number = 10
while (
        number <= 20):
        print(number)
        number += 1

#Decreament values
count = 110
while(
        count >= 100):
        print("number is", count)
        count -= 1

#For loop
for x in range(10):
    print(x)

for num in range(15, 20):
    print(num)

for b in range(1, 10, 2):
    print(b)

language = ["Python", "PHP", "Kotlin"]
for lang in language:
    print(lang)
    