#Arithmetic operators - used to perform simple mathematics operations
x = 10
y = 4
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)

#comparison operators
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
print(x == y) #equal to
print(x != y) # not equal to

#Assignment operators
w = 7
y = 12
print(w)

w += 2
print(w)

y *= 2
print(y)

#Logic operators - AND NOT OR
print(34 < 4 and 56 > 21) #Returns true if all the conditions are true
print(34 < 4 or 56 > 21) #Returns true if any of the condition is true
print(not (34 < 4 or 56 < 21))#Reverses the condition